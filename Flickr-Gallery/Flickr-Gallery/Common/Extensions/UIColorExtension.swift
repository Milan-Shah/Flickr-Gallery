import UIKit

extension UIColor {
    
    class var marinBlue: UIColor {
        return UIColor(red: 0.0, green: 56.0 / 255.0, blue: 99.0 / 255.0, alpha: 1.0)
    }
    
    class var cerulean: UIColor {
        return UIColor(red: 0.0, green: 139.0 / 255.0, blue: 191.0 / 255.0, alpha: 1.0)
    }
    
    class var gunmetal: UIColor {
        return UIColor(red: 71.0 / 255.0, green: 75.0 / 255.0, blue: 85.0 / 255.0, alpha: 1.0)
    }
}
